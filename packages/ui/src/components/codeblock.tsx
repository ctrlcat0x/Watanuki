export interface CodeBlockProps extends ComponentProps<'figure'> {
  /**
   * Icon of code block
   *
   * When passed as a string, it assumes the value is the HTML of icon
   */
  icon?: ReactNode;

  /**
   * Allow to copy code with copy button
   *
   * @defaultValue true
   */
  allowCopy?: boolean;

  /**
   * Keep original background color generated by Shiki or Rehype Code
   *
   * @defaultValue false
   */
  keepBackground?: boolean;

  viewportProps?: HTMLAttributes<HTMLElement>;

  /**
   * show line numbers
   */
  'data-line-numbers'?: boolean;

  /**
   * @defaultValue 1
   */
  'data-line-numbers-start'?: number;

  Actions?: (props: { className?: string; children?: ReactNode }) => ReactNode;
}

const TabsContext = createContext<{
  containerRef: RefObject<HTMLDivElement | null>;
  nested: boolean;
} | null>(null);

export function Pre(props: ComponentProps<'pre'>) {
  return (
    <pre {...props} className={cn('min-w-full w-max *:flex *:flex-col', props.className)}>
      {props.children}
    </pre>
  );
}

interface CodeBlockShellProps extends CodeBlockProps {
  actionsPlacement?: 'header' | 'overlay';
}

function CodeBlockShell({
  ref,
  title,
  allowCopy = true,
  keepBackground = false,
  icon,
  viewportProps = {},
  children,
  Actions = (props) => <div {...props} className={cn('empty:hidden', props.className)} />,
  actionsPlacement = title ? 'header' : 'overlay',
  ...props
}: CodeBlockShellProps) {
  const inTab = use(TabsContext) !== null;
  const areaRef = useRef<HTMLDivElement>(null);
  const actions = allowCopy ? (
    <CopyButton containerRef={areaRef} overlay={actionsPlacement === 'overlay'} />
  ) : null;

  return (
    <figure
      ref={ref}
      dir="ltr"
      {...props}
      tabIndex={-1}
      className={cn(
        'group/codeblock relative border shadow-sm not-prose overflow-hidden text-sm',
        inTab
          ? 'bg-fd-secondary -mx-px -mb-px last:rounded-b-xl'
          : 'my-4 bg-fd-card rounded-xl',
        keepBackground && 'bg-(--shiki-light-bg) dark:bg-(--shiki-dark-bg)',
        'shiki',
        props.className,
      )}
    >
      {title ? (
        <div className="flex h-9.5 items-center gap-2 border-b border-fd-border bg-fd-muted/60 px-4 text-fd-muted-foreground dark:bg-fd-muted/40">
          {typeof icon === 'string' ? (
            <div
              className="[&_svg]:size-3.5"
              dangerouslySetInnerHTML={{
                __html: icon,
              }}
            />
          ) : (
            icon
          )}
          <figcaption className="flex-1 truncate text-xs font-medium">{title}</figcaption>
          {Actions({
            className: '-me-2',
            children: actionsPlacement === 'header' ? actions : null,
          })}
        </div>
      ) : actionsPlacement === 'overlay' ? (
        Actions({
          className: cn(
            'absolute top-2 right-2 z-2 rounded-lg text-fd-muted-foreground',
            'opacity-0 transition-opacity',
            'group-hover/codeblock:opacity-100 group-focus-within/codeblock:opacity-100',
            'pointer-events-none group-hover/codeblock:pointer-events-auto group-focus-within/codeblock:pointer-events-auto',
          ),
          children: actions,
        })
      ) : (
        Actions({
          className: 'hidden',
          children: null,
        })
      )}
      <div
        ref={areaRef}
        {...viewportProps}
        role="region"
        tabIndex={0}
        className={cn(
          'text-[0.8125rem] py-3.5 overflow-auto max-h-[600px] fd-scroll-container focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-inset focus-visible:ring-fd-ring',
          viewportProps.className,
        )}
        style={
          {
            // space for toolbar
            '--padding-right': !title ? 'calc(var(--spacing) * 8)' : undefined,
            counterSet: props['data-line-numbers']
              ? `line ${Number(props['data-line-numbers-start'] ?? 1) - 1}`
              : undefined,
            ...viewportProps.style,
          } as object
        }
      >
        {children}
      </div>
    </figure>
  );
}

export function HeadlessCodeBlock(props: CodeBlockProps) {
  return <CodeBlockShell {...props} title={undefined} actionsPlacement="overlay" />;
}

export function CodeBlock(props: CodeBlockProps) {
  return <CodeBlockShell {...props} />;
}

