function CopyButton({
  className,
  containerRef,
  overlay = false,
  ...props
}: ComponentProps<'button'> & {
  containerRef: RefObject<HTMLElement | null>;
  overlay?: boolean;
}) {
  const t = useTranslations({ note: 'code block' });
  const [checked, onClick] = useCopyButton(() => {
    const pre = containerRef.current?.getElementsByTagName('pre').item(0);
    if (!pre) return;

    const clone = pre.cloneNode(true) as HTMLElement;
    clone.querySelectorAll('.nd-copy-ignore').forEach((node) => {
      node.replaceWith('\n');
    });

    void navigator.clipboard.writeText(clone.textContent ?? '');
  });

  return (
    <button
      type="button"
      data-checked={checked || undefined}
      className={cn(
        buttonVariants({
          className: 'hover:text-fd-accent-foreground data-checked:text-fd-accent-foreground',
          size: 'icon-xs',
        }),
        overlay &&
          'border border-fd-border/80 bg-fd-background/80 shadow-sm backdrop-blur-md hover:bg-fd-background/95',
        className,
      )}
      aria-label={
        checked ? t('Copied Text', { note: 'aria-label' }) : t('Copy Text', { note: 'aria-label' })
      }
      onClick={onClick}
      {...props}
    >
      {checked ? <Check className="size-4" /> : <CopyIcon className="size-4" />}
    </button>
  );
}

export function CodeBlockTabs({ ref, className, ...props }: ComponentProps<typeof Tabs>) {
  const containerRef = useRef<HTMLDivElement>(null);
  const nested = use(TabsContext) !== null;

  return (
    <Tabs
      ref={mergeRefs(containerRef, ref)}
      {...props}
      className={(s) =>
        cn(
          'bg-fd-card rounded-xl border',
          !nested && 'my-4',
          typeof className === 'function' ? className(s) : className,
        )
      }
    >
      <TabsContext
        value={useMemo(
          () => ({
            containerRef,
            nested,
          }),
          [nested],
        )}
      >
        {props.children}
      </TabsContext>
    </Tabs>
  );
}

export function CodeBlockTabsList({
  className,
  children,
  ...props
}: ComponentProps<typeof TabsList>) {
  return (
    <TabsList
      {...props}
      className={(s) =>
        cn(
          'relative flex flex-row px-2 overflow-x-auto text-fd-muted-foreground',
          typeof className === 'function' ? className(s) : className,
        )
      }
    >
      {children}
      <TabsIndicator
        className="absolute bottom-0 left-0 z-0 h-px w-(--active-tab-width) translate-x-(--active-tab-left) bg-fd-primary transition-[translate,width] duration-200 ease-[cubic-bezier(0.23,1,0.32,1)]"
        renderBeforeHydration
      />
    </TabsList>
  );
}

export function CodeBlockTabsTrigger({
  children,
  className,
  ...props
}: ComponentProps<typeof TabsTrigger>) {
  return (
    <TabsTrigger
      {...props}
      className={(s) =>
        cn(
          'relative z-1 group inline-flex text-sm font-medium text-nowrap items-center transition-colors gap-2 px-2 py-1.5 [&_svg]:size-3.5',
          s.active ? 'text-fd-primary' : 'hover:text-fd-accent-foreground',
          typeof className === 'function' ? className(s) : className,
        )
      }
    >
      {children}
    </TabsTrigger>
  );
}

export function CodeBlockTab({ className, ...props }: ComponentProps<typeof TabsContent>) {
  return (
    <TabsContent
      {...props}
      className={(s) => cn(typeof className === 'function' ? className(s) : className)}
    />
  );
}
